package dev.mathops.math.expr.layout;

/**
 * A container for the bounds of a simple expression component (one that is drawn rather than made up of glyphs).
 */
public class SimpleBounds {

    /** The minimum x coordinate. */
    double minX;

    /** The minimum y coordinate. */
    double minY;

    /** The maximum x coordinate. */
    double maxX;

    /** The maximum y coordinate. */
    double maxY;
}
