package dev.mathops.math.expr.structure;

/**
 * An accent that can be applied to a variable name.
 */
public enum EVariableAccent {

    /** No accent. */
    NONE,

    /** Vector arrow. */
    VECTOR,

    /** Single dot. */
    DOT,

    /** Double dot. */
    DOUBLE_DOT,

    /** Single Prime. */
    PRIME,

    /** Double Prime. */
    DOUBLE_PRIME,

    /** Triple Prime. */
    TRIPLE_PRIME,

    /** Hat. */
    HAT,

    /** Wide Hat. */
    WIDE_HAT,

    /** Tilde. */
    TILDE,

    /** Single overline. */
    OVERLINE,

    /** Double overline. */
    DOUBLE_OVERLINE,

    /** Single underline. */
    UNDERLINE,

    /** Double underline. */
    DOUBLE_UNDERLINE;
}
